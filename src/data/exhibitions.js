// Function to dynamically import all images from an exhibition folder
export const getExhibitionImages = async (folder) => {
  const images = import.meta.glob('/src/assets/images/exhibitions/**/*.{webp,png,jpg,jpeg}', { eager: true });
  
  const folderImages = Object.entries(images)
    .filter(([path]) => {
      // Match the folder name exactly to avoid matching subfolders
      const pathParts = path.split('/');
      const exhibitionsIndex = pathParts.indexOf('exhibitions');
      return exhibitionsIndex !== -1 && pathParts[exhibitionsIndex + 1] === folder;
    })
    .map(([path, module]) => ({
      path,
      src: module.default,
      name: path.split('/').pop().replace(/\.(webp|png|jpg|jpeg)$/, ''),
    }))
    .sort((a, b) => {
      // Sort by number if present in filename
      const numA = parseInt(a.name.match(/\d+/) || [0]);
      const numB = parseInt(b.name.match(/\d+/) || [0]);
      return numA - numB;
    });

  return folderImages;
};

